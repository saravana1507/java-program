package inheritence;

public class Calculator {
        
	public int addition(int i,int j)
	{
		int total=i+j;
		return total;
	}
	public int subraction(int i,int j)
	{
		int total=i-j;
		return total;
	}
	public int multipication(int i,int j)
	{
		int total=i*j;
		return total;
	}
	public int division(int i,int j)
	{
		int total=i/j;
		return total;
	}
}
